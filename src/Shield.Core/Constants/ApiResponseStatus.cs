namespace XkliburSolutions.Shield.Core.Constants;

/// <summary>
/// Defines the possible statuses for an API response.
/// </summary>
public class ApiResponseStatus
{
    /// <summary>
    /// Indicates a successful API response.
    /// </summary>
    public const string Success = "Success";

    /// <summary>
    /// Indicates an API response with a warning.
    /// </summary>
    public const string Warning = "Warning";

    /// <summary>
    /// Indicates an API response with an error.
    /// </summary>
    public const string Error = "Error";
}
